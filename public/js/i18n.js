export function t(key, lang) {
  const l = lang || localStorage.getItem('lang') || 'en';
  return strings[l]?.[key] ?? strings.en[key] ?? key;
}

export function applyLanguage(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (strings[lang]?.[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = strings[lang][key];
      } else {
        el.textContent = strings[lang][key];
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (strings[lang]?.[key]) el.placeholder = strings[lang][key];
  });

  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title');
    if (strings[lang]?.[key]) el.title = strings[lang][key];
  });
}

export function initLanguageSelector(selectId) {
  const select = document.getElementById(selectId);
  if (!select) return;

  const saved = localStorage.getItem('lang') || 'en';
  select.value = saved;
  applyLanguage(saved);

  select.addEventListener('change', () => {
    applyLanguage(select.value);
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: select.value }));
  });
}
